{
    struct IFEDemosaic36Titan480RegCmd1 demosaic36RegCmd1; ///< Demo Module config
    struct IFEDemosaic36Titan480RegCmd2 demosaic36RegCmd2; ///< Demo inter classifier
} CAMX_PACKED;
